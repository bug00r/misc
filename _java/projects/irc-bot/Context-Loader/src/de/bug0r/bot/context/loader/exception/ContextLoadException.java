package de.bug0r.bot.context.loader.exception;

public class ContextLoadException extends RuntimeException {

	public ContextLoadException() {
	}

	public ContextLoadException(String arg0) {
		super(arg0);
	}

	public ContextLoadException(Throwable arg0) {
		super(arg0);
	}

	public ContextLoadException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
